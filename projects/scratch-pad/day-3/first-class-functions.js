/** 
 * Given a startsWith character, which will be a single character, return a 
 * Function that tests whether a given String starts with the startsWith 
 * character.
 */
function createStartsWithFilter(startsWith) {
    // YOUR CODE BELOW HERE //
    //test character against string in nested function
    return function (string){
        //since it teests for starts with use charAt(0)
        //make both lowercase for certainty
        return string.charAt(0).toLowerCase() === startsWith.toLowerCase();
    };
    
    
    
    // YOUR CODE ABOVE HERE //
}

/** 
 * Given a endsWith character, which will be a single character, return a 
 * Function that tests whether a given String ends with the endsWith 
 * character.
 */
function createEndsWithFilter(endsWith) {
    // YOUR CODE BELOW HERE //
    //this is similar to the previous exercise except now testing at the last character
    //use charAt(string.length -1)
    return function (string){
        return string.charAt(string.length -1).toLowerCase() === endsWith.toLowerCase();
    };
    
    
    
    // YOUR CODE ABOVE HERE //
}

/** 
 * Given an Array of Strings and a Function designed to modify a String, 
 * return an Array of the Strings, modified.
 * 
 * TIP: You need to loop over the Strings, right? We need to pass each String to 
 * the modify Function, but we need to collect the results into some collection.
 */
function modifyStrings(strings, modify) {
    // YOUR CODE BELOW HERE //
    //this requires a container to push the strings modified by the given function modify
    var contain = [];
    
    for(var i = 0; i <= strings.length -1; i++){
        contain.push(modify(strings[i]));
    }
    return contain;
    
    
    
    // YOUR CODE ABOVE HERE //
}

/** 
 * Given an Array of Strings and a Function designed to test the String in some 
 * way and return a Boolean on whether it passed, return true if ALL Strings pass the test.
 * 
 * Imagine you had a list of names, and you wanted to test they all 
 * begin with "C", or they are all exclaimations that end with "!".
 * 
 * TIP: You need to loop over the Strings, right? And pass them to the test?
 */
function allStringsPass(strings, test) {
    // YOUR CODE BELOW HERE //
   // if all strings pass the test the count will remain 0
   //use a count variable to keep track of the passes
    var count = 0;
    //loop through the strings to collect falses
    for(var i = 0; i <= strings.length -1; i++){
        if(test(strings[i]) === false){
            count ++;
        }
    }
    
    if(count === 0){
        return true;
    }else{
        return false;
    }
    
    
    
    // YOUR CODE ABOVE HERE //
}

